package day_2;

public class Comparisionoperator {

	public static void main(String[] args) {
		int a=10,b=5;
		System.out.println("Equal operator: "+(a==b));
		System.out.println("Not Equal operator: "+(a!=b));
		System.out.println("LessthanEqual operator: "+(a<=b));
		System.out.println("GreaterthanEqual operator: "+(a>=b));
		System.out.println("Lessthan operator: "+(a<b));
		System.out.println("Greaterthan operator: "+(a>b));

	}

}
